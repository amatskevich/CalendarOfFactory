public enum MonthRus {

	JAN("ЯНВАРЬ"), FEB("ФЕВРАЛЬ"), MAR("МАРТ"), APR("АПРЕЛЬ"), MAY("МАЙ"), JUN("ИЮНЬ"), JUL("ИЮЛЬ"), AUG("АВГУСТ"), SEP(
			"СЕНТЯБРЬ"), OCT("ОКТЯБРЬ"), NOV("НОЯБРЬ"), DEC("ДЕКАБРЬ");

	public final String name;

	private MonthRus(String name) {
		this.name = name;
	}

}
